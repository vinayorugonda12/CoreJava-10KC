package method_questions;

public class AverageCalculator {
	
	
	
	static double averageOfThreeNumbers(double a,double b,double c) {
		
		double average=(a+b+c)/3;
		return average;
		
	}

	public static void main(String[] args) {
		System.out.println(" Average of three numbers is:"+averageOfThreeNumbers(1.2, 1.2, 1.3));

	}

}
